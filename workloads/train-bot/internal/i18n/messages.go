package i18n

import (
	"fmt"

	"telegramtrainapp/internal/domain"
)

type Catalog struct {
	messages map[domain.Language]map[string]string
}

const reportsChannelURL = "https://t.me/vivi_kontrole_reports"

func NewCatalog() *Catalog {
	return &Catalog{
		messages: map[domain.Language]map[string]string{
			domain.LanguageEN: enMessages,
			domain.LanguageLV: lvMessages,
		},
	}
}

func (c *Catalog) T(lang domain.Language, key string, args ...any) string {
	langMap, ok := c.messages[lang]
	if !ok {
		langMap = c.messages[domain.LanguageEN]
	}
	msg, ok := langMap[key]
	if !ok {
		msg = c.messages[domain.LanguageEN][key]
	}
	if len(args) == 0 {
		return msg
	}
	return fmt.Sprintf(msg, args...)
}

func (c *Catalog) Messages(lang domain.Language) map[string]string {
	langMap, ok := c.messages[lang]
	if !ok {
		langMap = c.messages[domain.LanguageEN]
	}
	out := make(map[string]string, len(langMap))
	for key, value := range langMap {
		out[key] = value
	}
	return out
}

